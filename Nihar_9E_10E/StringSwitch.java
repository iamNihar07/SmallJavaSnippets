
public class StringSwitch
{
    static void main()
    {
        String str="Xyz";
        str.endsWith("k");
        switch(str)
        {
            case "xyz":
            System.out.println("xyz");
            
            case "abc":
            System.out.println("xyz");
            
            default:
            System.out.println("Xyz");
            
            
        }
    }
}
       
        